# Arithmetic operators

num1 = 10
num2 = 20

# addition
print(f"Addition: {num1 + num2}")

# subtraction
print(f"Subtraction:{num1 - num2}")

# multiplication
print(f'Multiplication: {num1 * num2}')

# division
print(f"Division floating:{num2 / num1}")
print(f"Division integer:{num2 // num1}")

# modulus
print(f"Modulos:{num1 % num2}")

# exponent
print(f"Exponent:{3 ** 2}")