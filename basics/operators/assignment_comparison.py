# assignment operators
# =, +=, -=, *=, /=, //=, %=, **=, &=, |=, ^=, >>=, <<=

a = 5
b = 10

x,y,z = 1,2,3

print(x,y,z)


# shors hand operator

a += b  # a = a + b
print(a)

a -= b  # a = a - b
print(a)

a *= b  # a = a * b
print(a)

a //= b  # a = a / b
print(a)


# comparison operators
# ==, !=, >, <, >=, <=

print(a == b)  # False
print(a != b)  # True
print(a > b)  # False
print(a < b)  # True
print(a >= b)  # False
print(a <= b)  # True