# bitwise operators

# & - bitwise and
# | - bitwise or
# ^ - bitwise xor
# ~ - bitwise not
# << - left shift
# >> - right shift

x,y,z = 2,4,5

print(f"Bitwise AND: {x & y}") # 2
print(f"Bitwise OR: {x | y}")
print(f"Bitwise XOR: {x ^ y}")
print(f"Bitwise NOT: {~x}") # -(x+1)
print(f"Left shift: {x << 1}")
print(f"Right shift: {x >> 1}")